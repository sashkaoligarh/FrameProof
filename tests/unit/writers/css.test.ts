// Tests — :root {} wrapper
// ---------------------------------------------------------------------------

describe('generateCSS — :root {} wrapper', () => {
  const css = generateCSS(fullTokens);

  it('contains :root { selector', () => {
    expect(css).toContain(':root {');
  });

  it('contains closing brace for :root', () => {
    const rootIndex = css.indexOf(':root {');
    const afterRoot = css.slice(rootIndex);
    expect(afterRoot).toContain('}');
  });

  it('all custom properties are inside :root block', () => {
    // Extract the :root block
    const rootStart = css.indexOf(':root {');
    const rootEnd = css.lastIndexOf('}');
    const rootBlock = css.slice(rootStart, rootEnd + 1);

    // Find all -- declarations in the entire CSS
    const allVarDecls = [...css.matchAll(/--([\w-]+)\s*:/g)];
    // Find all -- declarations inside :root
    const rootVarDecls = [...rootBlock.matchAll(/--([\w-]+)\s*:/g)];

    expect(allVarDecls.length).toBe(rootVarDecls.length);
    expect(allVarDecls.length).toBeGreaterThan(0);
  });
});

