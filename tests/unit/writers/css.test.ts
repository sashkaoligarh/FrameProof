// Tests — Gradients NOT included in CSS (FR-007)
// ---------------------------------------------------------------------------

describe('generateCSS — does NOT include gradients (FR-007)', () => {
  const css = generateCSS(fullTokens);

  it('does not contain "gradient" in any variable name', () => {
    expect(css).not.toMatch(/--gradient/i);
  });

  it('does not contain a Gradients section comment', () => {
    expect(css).not.toMatch(/\/\*\s*Gradient/i);
  });

  it('does not output gradient stop colors as gradient tokens', () => {
    // The gradient has specific stop colors; ensure no gradient-specific section
    // (colors may appear in the colors section, but not as gradient tokens)
    const lines = css.split('\n');
    const gradientLines = lines.filter(
      (l) => l.includes('gradient') && !l.includes('Generated by'),
    );
    expect(gradientLines).toHaveLength(0);
  });
});

