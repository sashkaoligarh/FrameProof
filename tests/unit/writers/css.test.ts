// ---------------------------------------------------------------------------
// Tests — kebab-case variable names
// ---------------------------------------------------------------------------

describe('generateCSS — kebab-case variable names', () => {
  const css = generateCSS(fullTokens);

  it('all CSS custom properties use kebab-case (lowercase, hyphens, digits only)', () => {
    const varPattern = /--([\w-]+)\s*:/g;
    const matches = [...css.matchAll(varPattern)];
    expect(matches.length).toBeGreaterThan(0);

    for (const match of matches) {
      const name = match[1];
      // kebab-case: only lowercase letters, digits, and hyphens
      expect(name).toMatch(/^[a-z0-9]+(-[a-z0-9]+)*$/);
    }
  });

  it('color variables use --color-* pattern', () => {
    const colorVars = [...css.matchAll(/--color-([\w-]+)\s*:/g)];
    expect(colorVars.length).toBe(fullTokens.colors.length);
  });

  it('font variables use --font-* pattern', () => {
    const fontVars = [...css.matchAll(/--font-([\w-]+)\s*:/g)];
    expect(fontVars.length).toBeGreaterThan(0);
  });

  it('spacing variables use --spacing-* pattern', () => {
    const spacingVars = [...css.matchAll(/--spacing-([\w-]+)\s*:/g)];
    expect(spacingVars.length).toBe(fullTokens.spacing.length);
  });

  it('radius variables use --radius-* pattern', () => {
    const radiusVars = [...css.matchAll(/--radius-([\w-]+)\s*:/g)];
    expect(radiusVars.length).toBe(fullTokens.radii.length);
  });
});

